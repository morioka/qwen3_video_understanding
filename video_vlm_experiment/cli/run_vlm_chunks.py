from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

from video_vlm_experiment.io import load_json, write_json
from video_vlm_experiment.memory import RollingMemory
from video_vlm_experiment.prompts import CHUNK_ANALYSIS_PROMPT
from video_vlm_experiment.vlm import (
    VlmClient,
    api_key_from_args,
    build_chunk_messages,
    extract_message_text,
    parse_json_from_text,
)


def main() -> None:
    args = parse_args()
    prompt = _load_prompt(args.prompt)
    chunk_paths = _select_chunk_paths(args.chunk_input_dir, args.limit)
    client = None
    if not args.dry_run:
        model = args.model or ""
        if not model:
            raise ValueError("--model is required unless --dry-run is used")
        client = VlmClient(
            base_url=args.base_url,
            model=model,
            api_key=api_key_from_args(args.api_key, args.api_key_env),
            timeout_seconds=args.timeout_seconds,
        )

    raw_dir = args.output_dir / "raw"
    request_dir = args.output_dir / "requests"
    memory_path = args.output_dir / "memory.json"
    args.output_dir.mkdir(parents=True, exist_ok=True)
    raw_dir.mkdir(parents=True, exist_ok=True)
    if args.dry_run:
        request_dir.mkdir(parents=True, exist_ok=True)

    processed_count = 0
    rolling_memory = RollingMemory()
    for chunk_path in chunk_paths:
        output_path = args.output_dir / chunk_path.name
        if args.skip_existing and output_path.exists():
            if args.memory_mode == "rolling":
                existing_result = load_json(output_path)
                if not isinstance(existing_result, dict):
                    raise ValueError(f"{output_path} must contain a JSON object")
                rolling_memory.add_chunk_result(existing_result)
            continue

        chunk_input = load_json(chunk_path)
        if not isinstance(chunk_input, dict):
            raise ValueError(f"{chunk_path} must contain a JSON object")
        chunk_input = _normalize_frame_paths(chunk_input, chunk_path)
        previous_memory = None
        if args.memory_mode == "rolling":
            previous_memory = rolling_memory.to_prompt_text(args.memory_limit)

        messages = build_chunk_messages(
            chunk_input=chunk_input,
            prompt=prompt,
            previous_memory=previous_memory,
            include_images=not args.no_images,
            max_images=args.max_images,
        )

        if args.dry_run:
            write_json(
                request_dir / chunk_path.name,
                {
                    "model": args.model,
                    "messages": _redact_data_urls(messages),
                    "temperature": args.temperature,
                    "max_tokens": args.max_tokens,
                },
            )
            processed_count += 1
            continue

        assert client is not None
        response = client.create_chat_completion(
            messages,
            temperature=args.temperature,
            max_tokens=args.max_tokens,
            response_format_json=args.response_format_json,
        )
        text = extract_message_text(response)
        parsed = parse_json_from_text(text)
        if not isinstance(parsed, dict):
            raise ValueError(f"VLM response for {chunk_path} must be a JSON object")

        parsed.setdefault("chunk_index", chunk_input.get("chunk_index"))
        parsed.setdefault("chunk_start", chunk_input.get("chunk_start"))
        parsed.setdefault("chunk_end", chunk_input.get("chunk_end"))

        write_json(output_path, parsed)
        if args.memory_mode == "rolling":
            rolling_memory.add_chunk_result(parsed)
            write_json(memory_path, rolling_memory.to_dict())
        write_json(raw_dir / f"{chunk_path.stem}.response.json", response)
        (raw_dir / f"{chunk_path.stem}.message.txt").write_text(text, encoding="utf-8")
        processed_count += 1

    if args.memory_mode == "rolling":
        write_json(memory_path, rolling_memory.to_dict())
    print(f"processed {processed_count} chunk inputs into {args.output_dir}")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Send prepared chunk inputs to an OpenAI-compatible VLM.",
    )
    parser.add_argument(
        "--chunk-input-dir",
        required=True,
        type=Path,
        help="Directory containing chunk_*.json files generated by prepare-video-chunks.",
    )
    parser.add_argument(
        "--output-dir",
        required=True,
        type=Path,
        help="Directory for parsed VLM chunk analysis JSON files.",
    )
    parser.add_argument(
        "--base-url",
        default="http://localhost:8000/v1",
        help="OpenAI-compatible API base URL.",
    )
    parser.add_argument(
        "--model",
        default=None,
        help="Model name sent to the chat completions endpoint.",
    )
    parser.add_argument(
        "--api-key",
        default=None,
        help="API key value. Prefer --api-key-env for normal use.",
    )
    parser.add_argument(
        "--api-key-env",
        default="VLM_API_KEY",
        help="Environment variable containing the API key.",
    )
    parser.add_argument(
        "--prompt",
        type=Path,
        default=None,
        help="Prompt markdown file. Defaults to the built-in chunk analysis prompt.",
    )
    parser.add_argument(
        "--temperature",
        type=float,
        default=0.0,
        help="Sampling temperature.",
    )
    parser.add_argument(
        "--max-tokens",
        type=int,
        default=None,
        help="Optional max_tokens value.",
    )
    parser.add_argument(
        "--timeout-seconds",
        type=float,
        default=300.0,
        help="HTTP timeout per chunk request.",
    )
    parser.add_argument(
        "--max-images",
        type=int,
        default=None,
        help="Optional maximum number of frame images per chunk request.",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Optional maximum number of chunk files to process.",
    )
    parser.add_argument(
        "--skip-existing",
        action="store_true",
        help="Skip chunks whose parsed output file already exists.",
    )
    parser.add_argument(
        "--memory-mode",
        default="off",
        choices=("off", "rolling"),
        help="Memory carryover mode. Use rolling to pass previous memory_update entries to later chunks.",
    )
    parser.add_argument(
        "--memory-limit",
        type=int,
        default=20,
        help="Maximum previous memory items sent to each chunk when --memory-mode rolling is used.",
    )
    parser.add_argument(
        "--no-images",
        action="store_true",
        help="Send only chunk metadata and transcript text, without image payloads.",
    )
    parser.add_argument(
        "--response-format-json",
        action="store_true",
        help="Request OpenAI-compatible JSON object response_format.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Write request payload previews without calling the VLM endpoint.",
    )
    return parser.parse_args()


def _load_prompt(path: Path | None) -> str:
    if path is None:
        return CHUNK_ANALYSIS_PROMPT
    return path.read_text(encoding="utf-8")


def _select_chunk_paths(input_dir: Path, limit: int | None) -> list[Path]:
    paths = sorted(input_dir.glob("chunk_*.json"))
    if not paths:
        raise ValueError(f"no chunk_*.json files found in {input_dir}")
    if limit is not None:
        return paths[:limit]
    return paths


def _normalize_frame_paths(chunk_input: dict[str, Any], chunk_path: Path) -> dict[str, Any]:
    normalized = dict(chunk_input)
    frames = []
    for frame in normalized.get("frames", []):
        if not isinstance(frame, dict):
            continue
        copied = dict(frame)
        path = copied.get("path")
        if isinstance(path, str) and path:
            frame_path = Path(path)
            if not frame_path.exists():
                alternate = chunk_path.parent / frame_path
                if alternate.exists():
                    frame_path = alternate
            copied["path"] = str(frame_path)
        frames.append(copied)
    normalized["frames"] = frames
    return normalized


def _redact_data_urls(messages: list[dict[str, Any]]) -> list[dict[str, Any]]:
    redacted = []
    for message in messages:
        copied = dict(message)
        content = []
        for part in copied.get("content", []):
            if not isinstance(part, dict):
                content.append(part)
                continue
            part_copy = dict(part)
            if part_copy.get("type") == "image_url":
                part_copy["image_url"] = {"url": "<data-url-redacted>"}
            content.append(part_copy)
        copied["content"] = content
        redacted.append(copied)
    return redacted
